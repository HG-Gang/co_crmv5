    var localeAliases = {
        zh: 'zh-CN',
        'zh-cn': 'zh-CN',
        zh_CN: 'zh-CN',
        'zh_CN': 'zh-CN',
        en_US: 'en',
        'en-US': 'en'
    };
    var currentLocale = normalizeLocale(localStorage.getItem('crm_locale') || localStorage.getItem('front_lang') || 'zh-CN');
    var translations = {};
    var pendingLoads = {};

    function normalizeLocale(locale) {
        locale = locale || 'zh-CN';
        return localeAliases[locale] || locale;
    }

    function cloneObject(value) {
        return $.extend(true, {}, value || {});
    }

    function mergeWithFallback(locale, data) {
        return $.extend(true, cloneObject(fallbackTranslations[locale] || fallbackTranslations['zh-CN']), data || {});
    }

    function lookup(dict, key) {
        var keys = String(key || '').split('.');
        var result = dict;
        var i;

        for (i = 0; i < keys.length; i++) {
            if (result && typeof result === 'object' && Object.prototype.hasOwnProperty.call(result, keys[i])) {
                result = result[keys[i]];
            } else {
                return null;
            }
        }

        return typeof result === 'string' ? result : null;
    }

    function humanizeKey(key) {
        var last = String(key || '').split('.').pop() || '';

        return last.replace(/_/g, ' ').replace(/\b\w/g, function (letter) {
            return letter.toUpperCase();
        });
    }

    function rememberLocale(locale) {
        currentLocale = normalizeLocale(locale);
        localStorage.setItem('crm_locale', currentLocale);
        localStorage.setItem('front_lang', currentLocale);
        $('html').attr('lang', currentLocale);
    }

    function applyTranslatedValue($el, value) {
        var tagName = String($el.prop('tagName') || '').toUpperCase();

        if (tagName === 'INPUT' || tagName === 'TEXTAREA') {
            $el.attr('placeholder', value);
            return;
        }

        $el.text(value);
    }

    function translate(key) {
        var locale = normalizeLocale(currentLocale);
        var value = lookup(translations[locale], key);

        if (value !== null) {
            return value;
        }

        value = lookup(fallbackTranslations[locale], key);
        if (value !== null) {
            return value;
        }

        value = lookup(fallbackTranslations['zh-CN'], key);
        if (value !== null) {
            return value;
        }

        return humanizeKey(key);
    }

    return {
        setLocale: function (locale) {
            return this.loadLanguage(locale);
        },
        getLocale: function () {
            return currentLocale;
        },
        load: function (locale, data) {
            locale = normalizeLocale(locale);
            translations[locale] = mergeWithFallback(locale, data);
        },
        t: translate,
        updateUI: function () {
            var self = this;

            $('[data-translate]').each(function () {
                applyTranslatedValue($(this), self.t($(this).attr('data-translate')));
            });

            $('[data-translate-placeholder]').each(function () {
                $(this).attr('placeholder', self.t($(this).attr('data-translate-placeholder')));
            });

            $('[data-translate-title]').each(function () {
                $(this).attr('title', self.t($(this).attr('data-translate-title')));
            });

            $('html').attr('lang', currentLocale);
        },
        loadLanguage: function (locale) {
            locale = normalizeLocale(locale);

            if (translations[locale]) {
                rememberLocale(locale);
                CrmLang.updateUI();
                return Promise.resolve();
            }

            if (pendingLoads[locale]) {
                return pendingLoads[locale].then(function () {
                    rememberLocale(locale);
                    CrmLang.updateUI();
                });
            }

            pendingLoads[locale] = new Promise(function (resolve) {
                $.ajax({
                    url: '/js/common/lang/' + locale + '.js?v=2026052907',
                    type: 'GET',
                    dataType: 'text',
                    cache: true
                }).done(function (text) {
                    try {
                        eval(text);
                    } catch (e) {
                        translations[locale] = mergeWithFallback(locale, {});
                    }
                    if (!translations[locale]) {
                        translations[locale] = mergeWithFallback(locale, {});
                    }
                    rememberLocale(locale);
                    CrmLang.updateUI();
                    resolve();
                }).fail(function () {
                    translations[locale] = mergeWithFallback(locale, {});
                    rememberLocale(locale);
                    CrmLang.updateUI();
                    resolve();
                });
            });

            return pendingLoads[locale];
        },
        switchUI: function () {
            this.updateUI();
        }
    };
